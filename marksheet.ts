let percentage = 20;

if (percentage >= 90 && percentage < 100) {
  console.log("A++ Grade");
} else if (percentage >= 80 && percentage <= 89.99) {
  console.log("A+ Grade");
} else if (percentage >= 70 && percentage <= 79.99) {
  console.log("A Grade");
} else if (percentage >= 60 && percentage <= 69.99) {
  console.log("B Grade");
} else {
  console.log("Fail");
}
